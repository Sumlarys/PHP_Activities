<?php
//Usa una variable como condicional lógica.

// Declarar una variable booleana
$condicion = true;

// Usar la variable como condicional en un if
if ($condicion) {
    echo "La condición es verdadera.\n";
} else {
    echo "La condición es falsa.\n";
}


// Ejemplo con una cadena vacía
$cadenaVacia = "";

if ($cadenaVacia) {
    echo "La cadena no está vacía.\n";
} else {
    echo "La cadena está vacía y es considerada falsa.\n";
}
?>
