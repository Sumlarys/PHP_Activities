<?php
/**
 * Declara una variable booleana y 
 * usa un if para mostrar un mensaje dependiendo de su valor.
 */
// Declaración de la variable booleana
$esUsuarioActivo = true; 

// Usar una estructura if para mostrar un mensaje
if ($esUsuarioActivo) {
    echo "El usuario está activo.";
} else {
    echo "El usuario no está activo.";
}
?>
